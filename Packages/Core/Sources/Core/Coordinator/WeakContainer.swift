import UIKit

/// Слабая ссылка на объект-ссылочный тип с привязанным контекстом.
///
/// Удерживает объект слабо, поэтому после его освобождения `object` становится
/// `nil`. `context` позволяет связать с ссылкой произвольную метадату (например,
/// категорию делегата) и использовать её при фильтрации. Идентификатор `id`
/// сохраняет стабильную личность объекта даже после того, как слабая ссылка
/// обнулилась — это нужно для поиска и удаления записи по тому же объекту.
struct WeakContainer<T: AnyObject, Context> {
    /// Стабильный идентификатор объекта, не зависящий от жизни слабой ссылки.
    let id: ObjectIdentifier
    /// Слабая ссылка на объект; становится `nil` после его освобождения.
    weak let object: T?
    /// Произвольная метадата, связанная со ссылкой.
    let context: Context?

    init(_ object: T, context: Context? = nil) {
        self.id = ObjectIdentifier(object)
        self.object = object
        self.context = context
    }
}
